import UIKit

public func RGBCOLOR(r:CGFloat,g g:CGFloat,b b:CGFloat) -> UIColor {
    return UIColor(red: (r)/255.0, green: (g)/255.0, blue: (b)/255.0, alpha: 1.0)
}

